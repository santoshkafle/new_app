class Fruitmodel {
  final String name;
  final String price;
  final String imageUrl;
  final double rating;
  final String description;
  final String fruitUnit;
  final int maxAvailable;

  Fruitmodel({
    required this.name,
    required this.price,
    required this.imageUrl,
    required this.rating,
    required this.fruitUnit,
    required this.maxAvailable,
    required this.description,
  });
}
